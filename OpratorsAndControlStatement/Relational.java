import java.util.*;

public class Relational {
    public static void main(String[] args) {
        /* 
        Operator             Description                Example
        ==                   Is equal to                10==5 return false
        >                    Greater than               10 > 5 return true
        <                    Less than                  10 < 5 return false
        >=                   Greater than or equal to   10 >= 5 return true
        <=                   Less than or equal to      10 <= 5 return false
        !=                   Not equal to               10 != 5 return true
        
         */

    int x = 10;
    int y = 5;

    // == Operator
    System.out.println(x == y);// return false

    // > Operator
    System.out.println(x > y); // return true

    // < Operator
    System.out.println(x < y); // return false

    // >= Operator
    System.out.println(x >= y); // return true

    // <= Operator
    System.out.println(x <= y); // return false

    // != Operator 
    System.out.println(x != y); // return true



        
    }
    
}
